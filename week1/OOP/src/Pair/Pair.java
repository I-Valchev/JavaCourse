package Pair;

public class Pair <Object1, Object2>{
	private Object1 firstObject;
	private Object2 secondObject;
	
	public Object1 getFirstObject() {
		return firstObject;
	}
	public void setFirstObject(Object1 firstObject) {
		this.firstObject = firstObject;
	}
	public Object2 getSecondObject() {
		return secondObject;
	}
	public void setSecondObject(Object2 secondObject) {
		this.secondObject = secondObject;
	}
}
